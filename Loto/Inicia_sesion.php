	function checkLogin(){
		
		document.getElementById('email_error').style.display = 'none';
		document.getElementById('password_error').style.display = 'none';
		
		var email = document.getElementById('email').value;
		var password = document.getElementById('password').value;
		
		
		if(email==""){
			alert("Introduce un email")
		}
		else if(password==""){
			alert("Introduce el password")
		}else {
			
			$.ajax(
					{
						// Definimos la url						
						url:"form_login.php?email=" + email + "&password="+ password,
						type: "POST",

						success: function(data)
						{
							
							if (data==-1)
							{
								document.getElementById('email_error').style.display = 'block';
								
							}
							else if(data==0)
							{
							
								document.getElementById('password_error').style.display = 'block';
							}
							else{
								
								window.location.href="sesion.php?usuario="+ data;
							}
						}
					});
			
		}
		
		
	}
	
	function regenerar_contrasenya(){
		
		var re_email1 = document.getElementById('re_email1').value;
		var re_email2 = document.getElementById('re_email2').value;
		


		if(re_email1 ==''){
			 document.getElementById('re_email1').style.borderColor ='red';
		}
		else if(re_email2 ==''){
			 document.getElementById('re_email2').style.borderColor ='red';
		}
		else{
			if(re_email1 == re_email2 && re_email1!='' && re_email2!=''){
			
				
				$.ajax({
					url: "form_regenerar_pwd.php",
					type: "POST",
					data: {
						email: re_email1
					},
					success: function(data) {
						var clave = data;

						// Guardar los datos en el almacenamiento local
						localStorage.setItem("email", re_email1);
						localStorage.setItem("clave", clave);

						// Redireccionar a la página de destino
						window.location.href = "codigo_regenerar_pwd.php";
					},
					error: function() {
						alert('Ocurrió un error durante la solicitud. Por favor, intenta nuevamente.');
					}
				});

			}
			else{
				alert('El email de confirmación no coindice con el primer email intriducido')
			}
		}
		
		
	}
	
